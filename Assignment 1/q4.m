% ?????????????????????
% Initialization 
% ?????????????????????

clear all; clc; close all;

% converting all values in degrees to radians for ease of use later
theta = deg2rad(30);
phi = deg2rad(45);
delta = pi/2;
alpha = pi/5;
x = 0.5;

% ?????????????????????
% Main 
% ?????????????????????

A = 3*sin(theta)^2 - 5*cos(theta)^2;
B = sin(theta + phi) - 2*sin(theta)*cos(theta) + 2*cos(phi)*sin(phi);
C = sin(3*delta) - 3*sin(alpha)*cos(delta);
D = tan(theta - phi) - tan(phi)*cos(phi) + sin(theta)*sin(theta);
E = cos(4 * theta) + cos(theta)^4 - sin(theta)^4;
F = tan(delta) - sin(delta)/cos(delta);
G = asin(x)*acos(x)*(sqrt(1 - x));
H = 1 - (tan(delta)^3)*(sec(alpha)^2);
I = 1 + (cot(theta)^2)*csc(theta);
J = sinh(x) + 3*(1/2)*(2*x - 10^-5);

% ?????????????????????
% Post Processing
% ?????????????????????

fprintf('A = %f\n', A);
fprintf('B = %f\n', B);
fprintf('C = %f\n', C);
fprintf('D = %f\n', D);
fprintf('E = %f\n', E);
fprintf('F = %f\n', F);
fprintf('G = %f\n', G);
fprintf('H = %s\n', num2str(H));
fprintf('I = %f\n', I);
fprintf('J = %f\n', J);
